pattern = re.compile(u'([][\\\\#"$%&^_`{}~<>|\u0080-\uFFFD])')

def getPreamble():
    return """\
\\usepackage{textcomp}
\\newcommand{\Special}[1]{{\\fontencoding{T1}\\selectfont\\symbol{#1}}}
"""

def replace(match): return latexForUnicodeChar(match.group(1))

def latexForUnicodeChar(ch):
    code = ord(ch)
    if codeMap.has_key(code): return codeMap[code]
    if code >= 0xC0 and code <= 0xFF: return "\\Special{\"%02X}" % code
    return "[u%04X]" % code

def convert(s):
    s = re.sub(pattern, replace, s)
    return s.encode('ascii')

