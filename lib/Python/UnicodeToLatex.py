r"""
    Module which provides conversion of Unicode strings to LaTeX.
    Only a subset of the Unicode space is covered, including ASCII,
    all of Latin-1, and a few characters from Latin-2.

    Public function convert() takes a Unicode string and returns
    an ASCII LaTeX string.

    The LaTeX document in which these strings are inserted must
    have the following two lines in the preamble:

        \usepackage{textcomp}
        \newcommand{\Special}[1]{{\fontencoding{T1}\selectfont\symbol{#1}}}

    You can grab them with the public function getPreamble().

    It's also possible to use the module's latexForUnicodeChar() function
    directly to convert a single Unicode character (which is the only
    argument passed) to a LaTeX string.

    Note that this version does not provide the trick of alternating
    opening and closing quotes for u'"'.
"""

